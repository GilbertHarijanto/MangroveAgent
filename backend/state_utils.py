def extract_states(text: str) -> list[str]:
    lowered = text.lower()
    found = []

    # Match full state names first (longer names first to avoid overlaps)
    for name in sorted(STATE_NAME_TO_ABBR.keys(), key=len, reverse=True):
        if name in lowered:
            abbr = STATE_NAME_TO_ABBR[name]
            if abbr not in found:
                found.append(abbr)

    # Match abbreviations as whole words
    tokens = {token.strip(".,;:!?()[]{}\"'").upper()
              for token in text.split()}
    for token in tokens:
        if token in STATE_NAME_TO_ABBR.values() and token not in found:
            found.append(token)

    return found
